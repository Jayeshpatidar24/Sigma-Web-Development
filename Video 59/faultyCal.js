let num1 = 200;
let num2 = 23;
let sign = "+";

 


switch (sign) {
    case "+": console.log(num1/num2);
    
        break;

    case "-": console.log(num1*num2);
        
        break;
    case "*": console.log(num1+num2);
        
        break;    

    case "/": console.log(num1-num2);
        
        break;
    default:
        break;
}